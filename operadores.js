//Operadores lógicos
 // !, es un NOT se toma como falso lo que esta al frente

 !true ; //false

// = con este se asigna valores

var a = 1; //a vale 1

// == se usa para comparación, si tienen el mismo valor

3 == "3"; //true

// ===, compara si tienen el mismo tipo y valor

3 === "3"; //false

// <, menor que y > mayor que

5 < 3; //false

5 > 3; //true

// <=, menor o igual que y >=, mayor o igual que

5 >= 5; //true

5 <= 2; //false

// &&, true si los dos valores son verdaderos

5 > 3 && 2 < 4; //true

//  || , true si alguno de los dos es verdadero

5 > 3 || 2 < 1; //true

// ++, incrementa el valor uno en uno

